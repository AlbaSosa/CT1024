package ejemplo_Java;

public class Ejemplo_Id_Swich {

	public static void main(String[] args) {

		int i = 3;
		
		switch(i) {
		case 0:
			System.out.println("i es cero");
			break;
		case 1:
			System.out.println("i es uno");
			break;
		case 2:
			System.out.println("i es dos");
			break;
		default:
			System.out.println("I no equivale a ninguno de los anteriores");
			break;
		}

	}

}
